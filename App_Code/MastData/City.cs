using System;

namespace C2
{
    public class XXCity
    {
        private string code;
        private string name;
        private string country;

        public string Code
        {
            get { return this.code; }
            set { this.code = value; }
        }

        public string Name
        {
            get { return this.name; }
            set { this.name = value; }
        }

        public string Country
        {
            get { return this.country; }
            set { this.country = value; }
        }
    }
}
